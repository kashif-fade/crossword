function Crossword() {
  const persisted = useMemo(() => loadAppState().crossword, []);

  const [mode, setMode] = useState(() => persisted?.mode || "mini"); // "mini" | "midi"
  const [library, setLibrary] = useState(null); // array of 50 midi puzzles, fetched once
  const [libraryError, setLibraryError] = useState(false);
  const [midiIndex, setMidiIndex] = useState(() => persisted?.midiIndex || 0);

  // per-puzzle saved fill state, so switching puzzles (or reopening the
  // browser) restores exactly what was there before
  const [miniGrid, setMiniGrid] = useState(() => persisted?.miniGrid || null);
  const [midiGrids, setMidiGrids] = useState(() => persisted?.midiGrids || {});
  const [solvedIndices, setSolvedIndices] = useState(() => persisted?.solvedIndices || []);
  const [miniSolved, setMiniSolved] = useState(() => persisted?.miniSolved || false);

  useEffect(() => {
    fetch("/crosswords.json")
      .then((r) => r.json())
      .then((data) => setLibrary(data))
      .catch(() => setLibraryError(true));
  }, []);

  const puzzle = useMemo(() => {
    if (mode === "mini") return MINI_PUZZLE;
    if (!library) return null;
    const p = library[midiIndex];
    return { size: 11, label: `Midi #${midiIndex + 1} of ${library.length}`, ...p };
  }, [mode, library, midiIndex]);

  const [grid, setGrid] = useState([]);
  const [sel, setSel] = useState({ r: 0, c: 0 });
  const [dir, setDir] = useState("across");
  const [wrong, setWrong] = useState(new Set());

  // whenever the active puzzle changes (switching mode/index, or the
  // library finishing its fetch), load its saved progress if any exists,
  // otherwise start blank
  useEffect(() => {
    if (!puzzle) return;
    const saved = mode === "mini" ? miniGrid : midiGrids[midiIndex];
    setGrid(saved || puzzle.solution.map((row) => row.map((c) => (c === null ? null : ""))));
    setSel({ r: 0, c: 0 });
    setDir("across");
    setWrong(new Set());
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [puzzle]);

  // keep the per-puzzle saved snapshot in sync as the user types
  useEffect(() => {
    if (!puzzle || grid.length === 0) return;
    if (mode === "mini") setMiniGrid(grid);
    else setMidiGrids((g) => ({ ...g, [midiIndex]: grid }));
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [grid]);

  const empty = useCallback(
    () => (puzzle ? puzzle.solution.map((row) => row.map((c) => (c === null ? null : ""))) : []),
    [puzzle]
  );

  const switchToMini = () => setMode("mini");
  const switchToMidi = (index) => {
    setMode("midi");
    setMidiIndex(index);
  };
  const randomMidi = () => {
    if (!library) return;
    switchToMidi(Math.floor(Math.random() * library.length));
  };

  const activeCells = useMemo(
    () => (puzzle ? wordCells(puzzle, sel.r, sel.c, dir) : []),
    [puzzle, sel, dir]
  );
  const activeKey = new Set(activeCells.map(([r, c]) => `${r},${c}`));

  const solved = useMemo(
    () =>
      puzzle
        ? grid.every((row, r) =>
            row.every((v, c) => puzzle.solution[r][c] === null || v === puzzle.solution[r][c])
          )
        : false,
    [grid, puzzle]
  );

  // remember which puzzles have been solved before, so the library can show
  // a checkmark next to ones already completed
  useEffect(() => {
    if (!solved || !puzzle) return;
    if (mode === "mini") {
      if (!miniSolved) setMiniSolved(true);
    } else if (!solvedIndices.includes(midiIndex)) {
      setSolvedIndices((arr) => (arr.includes(midiIndex) ? arr : [...arr, midiIndex]));
    }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [solved, puzzle]);

  // persist everything needed to resume exactly where things were left off
  useEffect(() => {
    saveAppState({
      crossword: { mode, midiIndex, miniGrid, midiGrids, solvedIndices, miniSolved },
    });
  }, [mode, midiIndex, miniGrid, midiGrids, solvedIndices, miniSolved]);

  const activeClue = useMemo(() => {
    if (!puzzle || !activeCells.length) return null;
    const [sr, sc] = activeCells[0];
    return puzzle.clues[dir].find((cl) => cl.row === sr && cl.col === sc) || null;
  }, [activeCells, dir, puzzle]);

  const setCell = (r, c, val) => {
    setGrid((g) => {
      const n = g.map((row) => [...row]);
      n[r][c] = val;
      return n;
    });
    setWrong((w) => {
      if (!w.has(`${r},${c}`)) return w;
      const n = new Set(w);
      n.delete(`${r},${c}`);
      return n;
    });
  };

  const hiddenInputRef = useRef(null);
  const focusHiddenInput = () => {
    // must be synchronous, inside the actual tap handler -- iOS Safari only
    // shows the on-screen keyboard for a focus() call made directly within
    // a genuine user-gesture event, not deferred via setTimeout/promise
    if (hiddenInputRef.current) hiddenInputRef.current.focus();
  };

  const enterLetter = useCallback(
    (letter) => {
      if (!puzzle) return;
      const { r, c } = sel;
      if (!isBlock(puzzle, r, c)) {
        setCell(r, c, letter.toUpperCase());
        const idx = activeCells.findIndex(([ar, ac]) => ar === r && ac === c);
        if (idx >= 0 && idx < activeCells.length - 1) {
          const [nr, nc] = activeCells[idx + 1];
          setSel({ r: nr, c: nc });
        }
      }
    },
    [puzzle, sel, activeCells]
  );

  const backspace = useCallback(() => {
    const { r, c } = sel;
    if (grid[r] && grid[r][c]) setCell(r, c, "");
    else {
      const idx = activeCells.findIndex(([ar, ac]) => ar === r && ac === c);
      if (idx > 0) {
        const [pr, pc] = activeCells[idx - 1];
        setSel({ r: pr, c: pc });
        setCell(pr, pc, "");
      }
    }
  }, [sel, activeCells, grid]);

  const move = useCallback(
    (dr, dc) => {
      if (!puzzle) return;
      const size = puzzle.size;
      setSel((s) => {
        let { r, c } = s;
        for (let i = 0; i < size; i++) {
          r = Math.min(size - 1, Math.max(0, r + dr));
          c = Math.min(size - 1, Math.max(0, c + dc));
          if (!isBlock(puzzle, r, c)) return { r, c };
          if ((dr > 0 && r === size - 1) || (dr < 0 && r === 0)) break;
          if ((dc > 0 && c === size - 1) || (dc < 0 && c === 0)) break;
        }
        return s;
      });
    },
    [puzzle]
  );

  // physical keyboard (desktop)
  useEffect(() => {
    const onKey = (e) => {
      if (!puzzle) return;
      if (e.key === "ArrowUp") { setDir("down"); move(-1, 0); e.preventDefault(); }
      else if (e.key === "ArrowDown") { setDir("down"); move(1, 0); e.preventDefault(); }
      else if (e.key === "ArrowLeft") { setDir("across"); move(0, -1); e.preventDefault(); }
      else if (e.key === "ArrowRight") { setDir("across"); move(0, 1); e.preventDefault(); }
      else if (/^[a-zA-Z]$/.test(e.key)) { enterLetter(e.key); e.preventDefault(); }
      else if (e.key === "Backspace") { backspace(); e.preventDefault(); }
    };
    window.addEventListener("keydown", onKey);
    return () => window.removeEventListener("keydown", onKey);
  }, [move, enterLetter, backspace, puzzle]);

  // mobile virtual keyboard: a hidden, always-refocused input so iOS/Android
  // bring up their on-screen keyboard when a cell is tapped
  const onHiddenInputChange = (e) => {
    const val = e.target.value;
    e.target.value = "";
    const letters = val.replace(/[^a-zA-Z]/g, "");
    for (const ch of letters) enterLetter(ch);
  };
  const onHiddenInputKeyDown = (e) => {
    if (e.key === "Backspace") { backspace(); e.preventDefault(); }
    else if (e.key === "ArrowUp") { setDir("down"); move(-1, 0); e.preventDefault(); }
    else if (e.key === "ArrowDown") { setDir("down"); move(1, 0); e.preventDefault(); }
    else if (e.key === "ArrowLeft") { setDir("across"); move(0, -1); e.preventDefault(); }
    else if (e.key === "ArrowRight") { setDir("across"); move(0, 1); e.preventDefault(); }
  };

  const clickCell = (r, c) => {
    if (isBlock(puzzle, r, c)) return;
    focusHiddenInput();
    if (sel.r === r && sel.c === c) setDir((d) => (d === "across" ? "down" : "across"));
    else setSel({ r, c });
  };

  const clickClue = (cl, d) => {
    setDir(d);
    const cells = wordCells(puzzle, cl.row, cl.col, d);
    const firstEmpty = cells.find(([r, c]) => !grid[r][c]);
    const [r, c] = firstEmpty || cells[0];
    setSel({ r, c });
    focusHiddenInput();
  };

  const check = () => {
    if (!puzzle) return;
    const w = new Set();
    grid.forEach((row, r) =>
      row.forEach((v, c) => {
        if (puzzle.solution[r][c] !== null && v && v !== puzzle.solution[r][c]) w.add(`${r},${c}`);
      })
    );
    setWrong(w);
  };

  const reveal = () => {
    if (!puzzle) return;
    setGrid(puzzle.solution.map((row) => row.map((c) => (c === null ? null : c))));
    setWrong(new Set());
  };

  const clear = () => {
    setGrid(empty());
    setWrong(new Set());
  };

  if (!puzzle) {
    return (
      <p style={{ fontSize: 14, color: T.inkSoft }}>
        {libraryError
          ? "Couldn't load the crossword library. Try the Mini instead, or reload."
          : "Loading puzzles…"}
        {libraryError && (
          <>
            {" "}
            <Btn onClick={switchToMini}>Play Mini instead</Btn>
          </>
        )}
      </p>
    );
  }

  const cellSize = puzzle.size <= 5 ? "min(60px, 16vw)" : "min(38px, 8.2vw)";

  return (
    <div>
      {/* Invisible, always-refocused input so iOS/Android show their on-screen
          keyboard when a cell is tapped -- a bare div/click has nothing for
          mobile Safari to attach a keyboard to. */}
      <input
        ref={hiddenInputRef}
        value=""
        onChange={onHiddenInputChange}
        onKeyDown={onHiddenInputKeyDown}
        autoCapitalize="characters"
        autoCorrect="off"
        autoComplete="off"
        spellCheck="false"
        inputMode="text"
        tabIndex={-1}
        aria-label="Crossword letter entry"
        style={{
          position: "absolute",
          top: 0,
          left: -9999,
          width: 1,
          height: 1,
          border: "none",
          padding: 0,
          fontSize: 16, // >=16px keeps iOS Safari from auto-zooming on focus
        }}
      />

      {solved && <Banner>Solved — nicely done.</Banner>}

      <div style={{ display: "flex", gap: 8, marginBottom: 12, flexWrap: "wrap", alignItems: "center" }}>
        <span style={{ fontSize: 13, color: T.inkSoft, fontWeight: 600 }}>Puzzle:</span>
        <Btn onClick={switchToMini} primary={mode === "mini"}>
          Mini · 5×5{miniSolved ? " ✓" : ""}
        </Btn>
        <Btn onClick={() => switchToMidi(midiIndex)} primary={mode === "midi"} disabled={!library}>
          Midi · 11×11
        </Btn>
        {library && (
          <span style={{ fontSize: 13, color: T.inkSoft, marginLeft: "auto" }}>
            {solvedIndices.length} of {library.length} solved
          </span>
        )}
      </div>

      {mode === "midi" && library && (
        <div style={{ display: "flex", gap: 8, marginBottom: 16, flexWrap: "wrap", alignItems: "center" }}>
          <Btn onClick={() => switchToMidi((midiIndex - 1 + library.length) % library.length)}>
            ← Prev
          </Btn>
          <span style={{ fontSize: 13, color: T.inkSoft, minWidth: 100, textAlign: "center" }}>
            #{midiIndex + 1} of {library.length}
            {solvedIndices.includes(midiIndex) ? " ✓" : ""}
          </span>
          <Btn onClick={() => switchToMidi((midiIndex + 1) % library.length)}>Next →</Btn>
          <Btn onClick={randomMidi}>🔀 Shuffle</Btn>
        </div>
      )}

      <div style={{ display: "flex", flexWrap: "wrap", gap: 24, alignItems: "flex-start" }}>
        {/* Grid */}
        <div>
          <div
            style={{
              display: "grid",
              gridTemplateColumns: `repeat(${puzzle.size}, ${cellSize})`,
              border: `2px solid ${T.ink}`,
              width: "fit-content",
              background: T.ink,
              gap: 1,
            }}
          >
            {puzzle.solution.map((row, r) =>
              row.map((_, c) => {
                const key = `${r},${c}`;
                const block = isBlock(puzzle, r, c);
                const isSel = sel.r === r && sel.c === c;
                const inWord = activeKey.has(key);
                const isWrong = wrong.has(key);
                return (
                  <div
                    key={key}
                    onClick={() => clickCell(r, c)}
                    onTouchStart={focusHiddenInput}
                    style={{
                      width: cellSize,
                      height: cellSize,
                      background: block
                        ? T.block
                        : isSel
                        ? T.accentSel
                        : isWrong
                        ? T.wrongBg
                        : inWord
                        ? T.accentSoft
                        : T.panel,
                      position: "relative",
                      display: "flex",
                      alignItems: "center",
                      justifyContent: "center",
                      cursor: block ? "default" : "pointer",
                      fontSize: puzzle.size <= 5 ? "clamp(18px, 6vw, 26px)" : "clamp(12px, 3.6vw, 16px)",
                      fontWeight: 700,
                      color: isWrong ? T.wrongInk : T.ink,
                      userSelect: "none",
                    }}
                  >
                    {puzzle.numbers[key] && (
                      <span
                        style={{
                          position: "absolute",
                          top: 1,
                          left: 3,
                          fontSize: puzzle.size <= 5 ? 10 : 8,
                          fontWeight: 600,
                          color: T.inkSoft,
                        }}
                      >
                        {puzzle.numbers[key]}
                      </span>
                    )}
                    {!block && grid[r][c]}
                  </div>
                );
              })
            )}
          </div>

          {activeClue && (
            <div
              style={{
                marginTop: 12,
                padding: "8px 12px",
                background: T.accentSoft,
                borderRadius: 8,
                fontSize: 14,
                maxWidth: 320,
              }}
            >
              <strong>
                {activeClue.num}
                {dir === "across" ? "A" : "D"}.
              </strong>{" "}
              {activeClue.clue}
            </div>
          )}

          <div style={{ display: "flex", gap: 8, marginTop: 12, flexWrap: "wrap" }}>
            <Btn onClick={check}>Check</Btn>
            <Btn onClick={reveal}>Reveal</Btn>
            <Btn onClick={clear}>Clear</Btn>
          </div>
        </div>

        {/* Clues */}
        <div style={{ display: "flex", gap: 32, flexWrap: "wrap" }}>
          {["across", "down"].map((d) => (
            <div key={d} style={{ minWidth: 200 }}>
              <div
                style={{
                  fontSize: 12,
                  fontWeight: 700,
                  letterSpacing: "0.1em",
                  textTransform: "uppercase",
                  color: T.inkSoft,
                  marginBottom: 8,
                }}
              >
                {d}
              </div>
              {puzzle.clues[d].map((cl) => {
                const isActive =
                  activeClue &&
                  dir === d &&
                  activeClue.num === cl.num;
                return (
                  <div
                    key={cl.num}
                    onClick={() => clickClue(cl, d)}
                    style={{
                      padding: "6px 8px",
                      borderRadius: 6,
                      cursor: "pointer",
                      fontSize: 14,
                      background: isActive ? T.accentSoft : "transparent",
                      borderLeft: isActive
                        ? `3px solid ${T.accent}`
                        : "3px solid transparent",
                      marginBottom: 2,
                    }}
                  >
                    <strong>{cl.num}.</strong> {cl.clue}{" "}
                    <span style={{ color: T.inkSoft, fontSize: 12 }}>({cl.len})</span>
                  </div>
                );
              })}
            </div>
          ))}
        </div>
      </div>

      <p style={{ fontSize: 13, color: T.inkSoft, marginTop: 20 }}>
        Type to fill, arrow keys to move, tap a cell twice to switch direction.
        The 11×11 grids are drawn from a 50-puzzle library — each generated by
        a backtracking solver, filtered against a public quality/content
        blocklist, and clued mostly from WordNet definitions with manual
        touch-ups.
      </p>
    </div>
  );
}

