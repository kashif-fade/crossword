const isBlock = (puzzle, r, c) => puzzle.solution[r][c] === null;

function wordCells(puzzle, r, c, dir) {
  const size = puzzle.size;
  if (isBlock(puzzle, r, c)) return [];
  const dr = dir === "down" ? 1 : 0;
  const dc = dir === "across" ? 1 : 0;
  let sr = r, sc = c;
  while (sr - dr >= 0 && sc - dc >= 0 && !isBlock(puzzle, sr - dr, sc - dc)) { sr -= dr; sc -= dc; }
  const cells = [];
  let cr = sr, cc = sc;
  while (cr < size && cc < size && !isBlock(puzzle, cr, cc)) { cells.push([cr, cc]); cr += dr; cc += dc; }
  return cells;
}

// ---------------------------------------------------------------
// SUDOKU GENERATOR
// ---------------------------------------------------------------
function shuffle(a) {
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}
function sdValid(g, idx, v) {
  const r = Math.floor(idx / 9), c = idx % 9;
  for (let i = 0; i < 9; i++) {
    if (g[r * 9 + i] === v || g[i * 9 + c] === v) return false;
  }
  const br = r - (r % 3), bc = c - (c % 3);
  for (let i = 0; i < 3; i++)
    for (let j = 0; j < 3; j++)
      if (g[(br + i) * 9 + bc + j] === v) return false;
  return true;
}
function sdFindEmpty(g) {
  for (let i = 0; i < 81; i++) if (g[i] === 0) return i;
  return -1;
}
function sdPeers(idx) {
  const r = Math.floor(idx / 9), c = idx % 9;
  const br = r - (r % 3), bc = c - (c % 3);
  const peers = new Set();
  for (let i = 0; i < 9; i++) {
    peers.add(r * 9 + i);
    peers.add(i * 9 + c);
  }
  for (let i = 0; i < 3; i++)
    for (let j = 0; j < 3; j++)
      peers.add((br + i) * 9 + (bc + j));
  peers.delete(idx);
  return [...peers];
}
function sdFill(g) {
  const idx = sdFindEmpty(g);
  if (idx < 0) return true;
  for (const v of shuffle([1, 2, 3, 4, 5, 6, 7, 8, 9])) {
    if (sdValid(g, idx, v)) {
      g[idx] = v;
      if (sdFill(g)) return true;
      g[idx] = 0;
    }
  }
  return false;
}
function sdCountSolutions(g, limit) {
  const idx = sdFindEmpty(g);
  if (idx < 0) return 1;
  let count = 0;
  for (let v = 1; v <= 9; v++) {
    if (sdValid(g, idx, v)) {
      g[idx] = v;
      count += sdCountSolutions(g, limit - count);
      g[idx] = 0;
      if (count >= limit) return count;
    }
  }
  return count;
}
const SD_TARGETS = { easy: 40, medium: 32, hard: 26 };
function sdGenerate(difficulty) {
  const g = new Array(81).fill(0);
  sdFill(g);
  const solution = [...g];
  let givens = 81;
  const target = SD_TARGETS[difficulty];
  for (const p of shuffle([...Array(81).keys()])) {
    if (givens <= target) break;
    const backup = g[p];
    g[p] = 0;
    if (sdCountSolutions([...g], 2) !== 1) g[p] = backup;
    else givens--;
  }
  return { puzzle: [...g], solution };
}

