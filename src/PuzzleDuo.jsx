// ---------------------------------------------------------------
// PERSISTENCE — plain localStorage, since this is a real deployed app
// (not a Claude.ai artifact); wrapped in try/catch since private
// browsing or storage-disabled settings can throw.
// ---------------------------------------------------------------
const STORAGE_KEY = "puzzle-duo-state-v1";

function loadAppState() {
  try {
    const raw = localStorage.getItem(STORAGE_KEY);
    return raw ? JSON.parse(raw) : {};
  } catch {
    return {};
  }
}

function saveAppState(patch) {
  try {
    const current = loadAppState();
    const next = { ...current, ...patch };
    localStorage.setItem(STORAGE_KEY, JSON.stringify(next));
  } catch {
    // storage unavailable (private browsing, quota, etc) -- fail silently,
    // the app still works, it just won't remember between visits
  }
}

